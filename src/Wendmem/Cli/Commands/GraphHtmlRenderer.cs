    public static string Render(GraphData data)
    {
        var nodesJson = JsonSerializer.Serialize(data.Nodes, WendmemJsonContext.Default.ListGraphNode);
        var linksJson = JsonSerializer.Serialize(data.Links, WendmemJsonContext.Default.ListGraphLink);

        var sb = new StringBuilder(80 * 1024);

        // ── HEAD ──────────────────────────────────────────────
        sb.Append("""
<!DOCTYPE html>
<html lang="en">
<head>
<meta charset="UTF-8">
<meta name="viewport" content="width=device-width, initial-scale=1.0">

""");
        sb.AppendLine("<!-- generated by wendmem graph");
        sb.AppendLine($"     wing: {data.Wing}");
        sb.AppendLine($"     nodes: {data.Nodes.Count}  links: {data.Links.Count}");
        sb.AppendLine($"     at: {data.GeneratedAt:u} -->");
        sb.Append("""
<title>wendmem — knowledge palace</title>
<script src="https://cdnjs.cloudflare.com/ajax/libs/d3/7.8.5/d3.min.js"></script>

""");

        // ── CSS (verbatim from template) ──────────────────────
        sb.Append(CssBlock);

        sb.Append("""
</head>
<body>

""");

        // ── HTML BODY (verbatim from template) ────────────────
        sb.Append(HtmlBody);

        // ── SCRIPT ────────────────────────────────────────────
        sb.Append(JsHeader);

        sb.AppendLine();
        sb.Append("const nodes = ");
        sb.Append(nodesJson);
        sb.AppendLine(";");

        sb.AppendLine();
        sb.Append(JsLabelBuilder);

        sb.AppendLine();
        sb.Append("const links = ");
        sb.Append(linksJson);
        sb.AppendLine(";");

        sb.AppendLine();
        sb.Append(JsMain);

        sb.Append("""
</script>
</body>
</html>
""");

        return sb.ToString();
    }

