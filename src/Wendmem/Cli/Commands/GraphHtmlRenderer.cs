    private const string JsMain = """
// ── D3 SETUP ──────────────────────────────────────────────────────────────────
const graphEl = document.getElementById('graph');
const svg = d3.select('#svg');
const tooltip = document.getElementById('tooltip');

let width  = graphEl.clientWidth;
let height = graphEl.clientHeight;

const g = svg.append('g').attr('class', 'graph-root');

// zoom
const zoom = d3.zoom()
  .scaleExtent([0.15, 4])
  .on('zoom', e => g.attr('transform', e.transform));
svg.call(zoom);

// simulation
const sim = d3.forceSimulation(nodes)
  .force('link',   d3.forceLink(links).id(d => d.id).distance(d => {
    if (d.source.type === 'wing' && d.target.type === 'wing') return 240;
    if (d.target.type === 'triple') return 110;
    if (d.target.type === 'drawer') return 80;
    return 140;
  }).strength(0.6))
  .force('charge', d3.forceManyBody().strength(d => {
    if (d.type === 'wing')   return -600;
    if (d.type === 'wiki')   return -220;
    if (d.type === 'triple') return -140;
    return -80;
  }))
  .force('collide', d3.forceCollide().radius(d => TYPES[d.type].r * 1.8))
  .force('center',  d3.forceCenter(width / 2, height / 2))
  .force('x', d3.forceX(width / 2).strength(0.04))
  .force('y', d3.forceY(height / 2).strength(0.04));

// edges
const linkEls = g.append('g').attr('class', 'links')
  .selectAll('line')
  .data(links)
  .join('line')
    .attr('class', d => `link ${d.type}-link`);

// node groups
const nodeEls = g.append('g').attr('class', 'nodes')
  .selectAll('.node')
  .data(nodes)
  .join('g')
    .attr('class', d => `node ${d.type}-node`)
    .call(d3.drag()
      .on('start', dragStart)
      .on('drag',  dragged)
      .on('end',   dragEnd))
    .on('click',      onNodeClick)
    .on('mouseenter', onNodeEnter)
    .on('mouseleave', onNodeLeave);

nodeEls.append('circle')
  .attr('r', d => TYPES[d.type].r)
  .attr('fill', d => TYPES[d.type].color + '22')
  .attr('stroke', d => TYPES[d.type].color);

nodeEls.append('text')
  .attr('x', d => TYPES[d.type].r + 5)
  .attr('y', 0)
  .text(d => {
    const maxLen = d.type === 'wing' ? 20 : (d.type === 'triple' ? 30 : 22);
    const lbl = d.label || '';
    return lbl.length > maxLen ? lbl.slice(0, maxLen) + '…' : lbl;
  })
  .attr('fill', d => TYPES[d.type].color + 'cc')
  .style('display', d => ['drawer','triple'].includes(d.type) ? 'none' : null);

sim.on('tick', () => {
  linkEls
    .attr('x1', d => d.source.x)
    .attr('y1', d => d.source.y)
    .attr('x2', d => d.target.x)
    .attr('y2', d => d.target.y);
  nodeEls.attr('transform', d => `translate(${d.x},${d.y})`);
});

// ── interactions ──────────────────────────────────────────────────────────────
let selectedNode = null;
let activeFilters = new Set(Object.keys(TYPES)); // all on by default

function getNeighbourIds(node) {
  const ids = new Set([node.id]);
  links.forEach(l => {
    const s = l.source.id || l.source;
    const t = l.target.id || l.target;
    if (s === node.id) ids.add(t);
    if (t === node.id) ids.add(s);
  });
  return ids;
}

function onNodeClick(e, d) {
  e.stopPropagation();
  if (selectedNode && selectedNode.id === d.id) {
    // deselect
    selectedNode = null;
    nodeEls.classed('selected faded', false);
    linkEls.classed('highlighted faded', false);
    showEmptyState();
    return;
  }
  selectedNode = d;
  const neighbours = getNeighbourIds(d);
  nodeEls
    .classed('selected', n => n.id === d.id)
    .classed('faded',    n => !neighbours.has(n.id));
  linkEls
    .classed('highlighted', l => (l.source.id||l.source)===d.id || (l.target.id||l.target)===d.id)
    .classed('faded',       l => (l.source.id||l.source)!==d.id && (l.target.id||l.target)!==d.id);
  showNodeDetail(d, neighbours);
}

svg.on('click', () => {
  if (selectedNode) {
    selectedNode = null;
    nodeEls.classed('selected faded', false);
    linkEls.classed('highlighted faded', false);
    showEmptyState();
  }
});

function onNodeEnter(e, d) {
  if (selectedNode) return;
  tooltip.style.display = 'block';
  tooltip.style.left = (e.offsetX + 14) + 'px';
  tooltip.style.top  = (e.offsetY - 6)  + 'px';
  let html = `<strong>${d.label || d.name || ''}</strong>`;
  if (d.type === 'triple') html = `<strong>${d.subject}</strong> <span style="color:var(--gold)">${d.predicate}</span> ${d.object}`;
  if (d.type === 'drawer') html += `<br><span style="color:var(--dim)">${d.source}</span>`;
  tooltip.innerHTML = html;
}

function onNodeLeave() {
  tooltip.style.display = 'none';
}

function dragStart(e, d) {
  if (!e.active) sim.alphaTarget(0.3).restart();
  d.fx = d.x; d.fy = d.y;
}
function dragged(e, d)  { d.fx = e.x; d.fy = e.y; }
function dragEnd(e, d)  {
  if (!e.active) sim.alphaTarget(0);
  d.fx = null; d.fy = null;
}

