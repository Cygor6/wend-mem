    const outcomeColor = d.outcome === 'success' ? 'var(--sage)' : d.outcome === 'failure' ? 'var(--rose)' : 'var(--gold)';
    html += `
      <div class="detail-meta"><span class="key">outcome</span><span style="color:${outcomeColor}">${d.outcome}</span></div>
      <div class="detail-meta"><span class="key">wing</span>${d.wing}</div>
    `;
    if (d.nextTime) html += `<div class="detail-content">${d.nextTime}</div>`;
  }
  else if (d.type === 'skill') {
    const pct = Math.round(d.successRate * 100);
    html += `
      <div class="detail-meta"><span class="key">path</span>${d.path || ''}</div>
      <div class="detail-meta"><span class="key">success</span><span style="color:var(--sage)">${pct}%</span></div>
      <div class="detail-meta"><span class="key">wing</span>${d.wing}</div>
    `;
    if (d.desc) html += `<div class="detail-content">${d.desc}</div>`;
  }

  // connections
  if (connectedNodes.length) {
    html += `<h3 style="font-size:9px;letter-spacing:.15em;text-transform:uppercase;color:var(--dim);margin-top:14px;margin-bottom:6px">${connectedNodes.length} connections</h3>
    <ul class="connections-list">`;
    connectedNodes.slice(0,10).forEach(n => {
      const col = TYPES[n.type].color;
      const lbl = (n.label || n.name || '').slice(0, 32);
      html += `<li onclick="selectNodeById('${n.id}')"><div class="dot" style="background:${col}"></div>${lbl}<span class="lbl" style="margin-left:auto">${n.type}</span></li>`;
    });
    if (connectedNodes.length > 10) html += `<li style="color:var(--muted);cursor:default">+${connectedNodes.length-10} more</li>`;
    html += '</ul>';
  }

  det.innerHTML = html;
}

function getTitle(d) {
  if (d.type === 'wing')    return d.label;
  if (d.type === 'wiki')    return d.title || d.label;
  if (d.type === 'triple')  return `${d.subject} → ${d.object}`;
  if (d.type === 'drawer')  return d.label;
  if (d.type === 'episode') return d.goal || d.label;
  if (d.type === 'skill')   return d.name || d.label;
  return d.label || d.id;
}

window.selectNodeById = function(id) {
  const d = nodes.find(n => n.id === id);
  if (!d) return;
  const neighbours = getNeighbourIds(d);
  selectedNode = d;
  nodeEls
    .classed('selected', n => n.id === d.id)
    .classed('faded',    n => !neighbours.has(n.id));
  linkEls
    .classed('highlighted', l => (l.source.id||l.source)===d.id || (l.target.id||l.target)===d.id)
    .classed('faded',       l => (l.source.id||l.source)!==d.id && (l.target.id||l.target)!==d.id);
  showNodeDetail(d, neighbours);
  // pan to node
  svg.transition().duration(400).call(
    zoom.transform,
    d3.zoomIdentity
      .translate(width/2, height/2)
      .scale(1.2)
      .translate(-d.x, -d.y)
  );
};

