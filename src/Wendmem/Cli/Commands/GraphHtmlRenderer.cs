function toggleFilter(key) {
  if (activeFilters.has(key)) {
    if (activeFilters.size === 1) return; // keep at least one
    activeFilters.delete(key);
  } else {
    activeFilters.add(key);
  }
  applyFilters();
}

function applyFilters() {
  Object.keys(TYPES).forEach(key => {
    const btn = document.getElementById(`filter-${key}`);
    if (btn) btn.classList.toggle('active', activeFilters.has(key));
  });
  nodeEls.style('display', d => activeFilters.has(d.type) ? null : 'none');
  linkEls.style('display', l => {
    const sn = nodes.find(n => n.id === (l.source.id||l.source));
    const tn = nodes.find(n => n.id === (l.target.id||l.target));
    return sn && tn && activeFilters.has(sn.type) && activeFilters.has(tn.type) ? null : 'none';
  });
  updateStats();
}

